%создание несимметричной матрицы с заданными собственными числами
function A = generate_matrix(n, v)
%     B = [9.0 1.3 0.5 0.2 1.5;
%     0.6 14.0 1.1 0.7 1.4;
%     0.4 1.2 13.0 0.7 1.3;
%     1.1 1.1 1.9 18.0 1.2;
%     8 1.8 1.6 20.0 1.1];
    D = diag(v);
    [Q,R] = qr(rand(n,n));
    A = Q*D*Q';
end
